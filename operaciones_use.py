def operacion_and(numero1, numero2):
    return f'El resultado AND es: {numero1 & numero2}'
def operacion_or(numero1,numero2):
    return f'El resultado OR es: {numero1 | numero2}'
def operacion_xor(numero1,numero2):
    return f'El resultado XOR es: {numero1 ^ numero2}'
def operacion_not(numero1):
    return f'El resultado NOT es: {~numero1}'
def desplazamiento_right(numero1,numero2):
    return f'El resultado de la operacion shift right es: {numero1>>numero2}'
def desplazamiento_left(numero1,numero2):
    return f'El resultado de la operacion shift left es: {numero1<<numero2}'